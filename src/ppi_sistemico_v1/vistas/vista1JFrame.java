    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ficha = (String) jComboBox1.getSelectedItem();
        Integer num1 = Integer.parseInt(ficha.split(":")[0]);
        Integer num2 = Integer.parseInt(ficha.split(":")[1]);
        String dir = "";
        if (jRadioButton3.isSelected()) {
            dir = "I";
        } else {
            if (jRadioButton4.isSelected()) {
                dir = "D";
            }
        }
        if (dir.equals("")) {
            JOptionPane.showMessageDialog(this, "Tiene que seleccionar un Lado");
        } else {
            imprimir(juego.jugarJugador(num1, num2, dir));
            imprimir(juego.jugarComputador());
            imprimir(juego.inspeccionar());
            pintar();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

