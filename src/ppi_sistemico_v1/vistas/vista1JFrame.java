    public void iniciarJuego() {
        String NFS = jTextField1.getText();
        try {
            Integer NF = Integer.parseInt(NFS);
            if (NF >= 14) {
                JOptionPane.showMessageDialog(this, "El Número de Fichas incio: tiene que ser un número menor o igual a 14");
            } else {
                juego = new Juego(NF, 0);
                juego.crearFichas();
                juego.repartirJuego();
                imprimir(juego.repartirTurno());
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "El Número de Fichas incio: tiene que ser un número");
        }
        jButton2.setEnabled(true);
        jButton3.setEnabled(true);

    }

    public void imprimir(String res) {
        if (!res.equals("")) {
            JOptionPane.showMessageDialog(this, res);
        }
    }

